# ======================
# 选择“最符合的一个维度”
# ======================
def tag_best_dimension(text: str):
    if not isinstance(text, str):
        text = ""
    low = text.lower()

    hits = []

    for dim_id, dim_info in VALUE_LEXICON.items():
        for sub_code, sub_info in dim_info["sub"].items():
            for kw in sub_info["keywords"]:
                if kw.lower() in low:
                    hits.append(sub_code)

    if not hits:
        return None, None

    # 多个维度 → 选择最前面的（1~12）
    best = sorted(hits, key=lambda x: (int(x.split(".")[0]), float(x.split(".")[1])))[0]
    dim_id = best.split(".")[0]
    return DIM_ID_TO_NAME[dim_id], best


# ======================
# 主流程：打标签 + 删除未命中
# ======================
def process_file(input_file, output_file):
    df = pd.read_excel(input_file, dtype=str)
    df["标题"] = df["标题"].fillna("")
    df["内容"] = df["内容"].fillna("")

    chosen_dim = []
    sub_codes = []

    for _, row in df.iterrows():
        full = f"{row['标题']}。{row['内容']}"
        dim, sub = tag_best_dimension(full)
        chosen_dim.append(dim)
        sub_codes.append(sub)

    df["维度"] = chosen_dim
    df["子类"] = sub_codes

    # 删除未命中的
    before = len(df)
    df = df.dropna(subset=["维度"])
    after = len(df)
    removed = before - after

    df.to_excel(output_file, index=False)
    print(f"处理完成！ 删除无标签样本：{removed} 条。 输出：{output_file}")


if __name__ == "__main__":
    process_file("cleaned.xlsx", "tagged.xlsx")
